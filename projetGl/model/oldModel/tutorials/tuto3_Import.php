<h2><a href="#">Importation of python to pseudo code</a></h2>
The system can also <strong>import</strong> some <strong>python</strong> code. It's use to transform python <strong>to pseudo code</strong>. Like that we can analyse the pseudo code and see what the python code do. To do that you just need to <strong>click</strong> on "Load Python" (on the write panel).<br />
It will <strong>open a new windows</strong> where you can write code and click on <strong>import</strong> to import the python into the pseudo code.
<h2><a href="#">Example </a></h2>
In the <strong>exemple</strong> will use all kinds of structure and even the <strong>main function</strong> of the list in python.
<ul>
    <li>
        <h3>Python</h3>
        <ul>
            <li>one = 1</li>
            <li>two = 2</li>
            <li>three = one + two</li>
            <li>a = 1 + 2 + ( 9 * 2 + ( ( 8 - 10 ) + 3 ) )</li>
            <li>b = [1, 5, 12]</li>
            <li>b.append(1)</li>
            <li>a = len(b)</li>
            <li>srt1 = "hello"</li>
            <li>chaine = "world"</li>
            <li>conc = srt1 + " " + chaine</li>
        </ul>
    </li>
    <li>
        <h3>Pseudo-code</h3>
        <ul>
            <li>init one</li>
            <li>one <- 1</li>
            <li>init two</li>
            <li>two <- 2</li>
            <li>init three</li>
            <li>three <- one + two</li>
            <li>init a</li>
            <li>a <- 1 + 2 + ( 9 * 2 + ( ( 8 - 10 ) + 3 ) )</li>
            <li>init b[]</li>
            <li>b[0] <- 1</li>
            <li>b[1] <- 5</li>
            <li>b[2] <- 12</li>
            <li>b[3] <- 1</li>
            <li>a <- 4</li>
            <li>init srt1</li>
            <li>srt1 <- hello</li>
            <li>init chaine</li>
            <li>chaine <- world</li>
            <li>init conc</li>
            <li>conc <- srt1 + " " + chaine</li>
        </ul>
    </li>
</ul>
<br />
Like we can see the function like <strong>len()</strong> or <strong>.append()</strong> are not the same in pseudo code. We change the function to have the <strong>same result</strong>. But by two different way. The importation of python is only the first part of the system. Because now you can use the execution part to see what each line do to the others. Like that the system is able to <strong>explain</strong> line by line the python by changing him in pseudo code first.
